#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print(char x, char y, char z)
{
	ft_putchar(x);
	ft_putchar(y);
	ft_putchar(z);
}

void	ft_print_comb(void)
{
	char	x;
	char	y;
	char	z;

	x = '0';
	while (x <= '7')
	{
		y = x + 1;
		while (y <= '8')
		{
			z = y + 1;
			while (z <= '9')
			{
				ft_print(x, y, z);
				if (!(x == '7' && y == '8' && z == '9'))
				{
					write(1, ", ", 2);
				}
				z++;
			}
			y++;
		}
		x++;
	}
	ft_putchar('\n');
}
